func NarrativeOrder[T any](items []T, order func(T) int) []T {
	cp := make([]T, len(items))
	copy(cp, items)
	sort.SliceStable(cp, func(i, j int) bool {
		return order(cp[i]) < order(cp[j])
	})
	return cp
}

