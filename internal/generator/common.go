const UnresolvedReader = "(unresolved-reader)"

const RoleOperator = "operator"

const RoleDomainSteward = "domain-steward"

const RoleFrameworkMaintainer = "framework-maintainer"

var DocReaderRoles = map[string]string{
	"CONSTITUTION":         RoleOperator,
	"OPEN":                 RoleOperator,
	"UNENFORCED":           RoleOperator,
	"ATOMS_OPERATOR":       RoleOperator,
	"ATOMS_SUBSTRATE":      RoleOperator,
	"ATOMS_DISCIPLINE":     RoleOperator,
	"ATOMS_CHECK":          RoleOperator,
	"SHARED_THINKING":      RoleOperator,
	"SHARED_TOOL":          RoleOperator,
	"REQUIREMENTS":         RoleDomainSteward,
	"TENSIONS":             RoleDomainSteward,
	"DECISIONS":            RoleDomainSteward,
	"HISTORY":              RoleDomainSteward,
	"ENTITIES":             RoleDomainSteward,
	"FRAMEWORK_INVARIANTS": RoleFrameworkMaintainer,
	"GLOSSARY":             RoleFrameworkMaintainer,
	"AUDIT":                RoleFrameworkMaintainer,
	"REPO_MAP":             RoleOperator,
}

var DomainDocReaders = map[string]string{
	RoleOperator:            "ai-agent",
	RoleDomainSteward:       "domain-user",
	RoleFrameworkMaintainer: "framework-author",
}

func ResolveReader(docKind string, stakeholderIDs map[string]struct{}, roleBindings map[string]string) string {
	role, ok := DocReaderRoles[docKind]
	if !ok {
		return UnresolvedReader
	}
	if roleBindings == nil {
		return UnresolvedReader
	}
	boundID, ok := roleBindings[role]
	if !ok {
		return UnresolvedReader
	}
	if _, exists := stakeholderIDs[boundID]; !exists {
		return UnresolvedReader
	}
	return boundID
}

func ReaderHeaderLine(docKind string, g *ontology.Graph) string {
	return "reader: " + ResolveReader(docKind, ontology.StakeholderIDs(g), DomainDocReaders)
}

func Cell(text string) string {
	t := strings.ReplaceAll(text, "\n", " ")
	t = strings.ReplaceAll(t, "|", "\\|")
	return t
}

func MermaidID(nodeID string) string {
	t := strings.ReplaceAll(nodeID, "-", "_")
	t = strings.ReplaceAll(t, "~", "_")
	t = strings.ReplaceAll(t, ".", "_")
	return t
}
