# 5. Empty content -> calm "no content yet" notice, not an awkward empty table
# ---------------------------------------------------------------------------


def test_empty_graph_renders_no_content_notice() -> None:
    """An empty graph yields a calm "no content yet" notice in each doc.

    This is what the committed docs/gen/ files should read on a freshly-shipped
    framework — a calm "load a domain" prompt, not an empty roster.
    """
    from tensio.graph import TensionGraph  # noqa: PLC0415

    g = TensionGraph()
    assert g.is_empty()
    for build in (
        gen_spec.build_requirements,
        gen_spec.build_tensions,
        gen_spec.build_open,
        gen_spec.build_unenforced,
        gen_spec.build_history,
        gen_spec.build_decisions,
    ):
        text = build(g)
        assert "No domain content loaded" in text, (
            f"{build.__name__} must render the empty-content notice"
        )
