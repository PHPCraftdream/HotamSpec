# 3. Canon coverage: every public object carries a §-label
# ---------------------------------------------------------------------------


def _public_objects() -> list[tuple[str, str]]:
    """All public tensio objects: (qualified name, docstring).

    Objects = module docstrings + top-level public classes/functions. Methods
    are covered by their class docstring and excluded.
    """
    objects: list[tuple[str, str]] = []
    for path in sorted(gen_spec.SRC.glob("*.py")):
        tree = ast.parse(_read_normalized(path))
        mod = path.stem
        objects.append((f"tensio.{mod}", ast.get_docstring(tree) or ""))
        for node in tree.body:
            if isinstance(node, ast.ClassDef) and not node.name.startswith("_"):
                objects.append(
                    (f"tensio.{mod}.{node.name}", ast.get_docstring(node) or "")
                )
            elif isinstance(
                node, (ast.FunctionDef, ast.AsyncFunctionDef)
            ) and not node.name.startswith("_"):
                objects.append(
                    (f"tensio.{mod}.{node.name}", ast.get_docstring(node) or "")
                )
    return objects


# Explicit list of canon sections that MUST appear across the docstrings.
REQUIRED_SECTIONS = [
    "§Requirement",
    "§Conflict",
    "§Assumption",
    "§Axis",
    "§Stakeholder",
    "§Invariants",
    "§Graph",
]


def test_all_sections_covered_in_docstrings() -> None:
    """Each required §-section appears somewhere in the tensio docstrings."""
    blob = "\n".join(doc for _name, doc in _public_objects())
    missing = [s for s in REQUIRED_SECTIONS if s not in blob]
    assert not missing, f"§-sections not covered by docstrings: {missing}"


@pytest.mark.parametrize("name,doc", _public_objects())
def test_public_object_has_section_label(name: str, doc: str) -> None:
    """Every public object has a non-empty docstring carrying a `Canon:§` label."""
    assert doc.strip(), f"{name}: empty docstring"
    assert "§" in doc, f"{name}: docstring missing a `Canon:§N` label"


