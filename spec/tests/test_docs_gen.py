def test_tensions_md_contains_conflict_nodes_and_mermaid_on_demo() -> None:
    """TENSIONS.md against the demo renders every fixture conflict + Mermaid."""
    g = seed_graph()
    text = gen_spec.build_tensions(g)
    assert "```mermaid" in text, "TENSIONS.md missing Mermaid diagram"
    for c in g.conflicts:
        assert c.id in text, f"TENSIONS.md missing conflict node {c.id}"
        assert c.axis in text, f"TENSIONS.md missing axis {c.axis}"


def test_requirements_md_contains_every_requirement_on_demo() -> None:
    """REQUIREMENTS.md against the demo lists every fixture requirement id."""
    g = seed_graph()
    text = gen_spec.build_requirements(g)
    for r in g.requirements:
        assert r.id in text, f"REQUIREMENTS.md missing requirement {r.id}"


def test_open_md_lists_open_items_on_demo() -> None:
    """OPEN.md against the demo lists every OPEN req + every unresolved conflict."""
    g = seed_graph()
    text = gen_spec.build_open(g)
    for r in g.requirements:
        if r.is_open():
            assert r.id in text, f"OPEN.md missing open requirement {r.id}"
    for c in g.conflicts:
        if c.is_unresolved():
            assert c.id in text, f"OPEN.md missing unresolved conflict {c.id}"


