def test_generated_files_are_lf_and_banner(tmp_path: Path) -> None:
    """Generator writes LF without CR and stamps the AUTOGENERATED banner."""
    g = gen_spec.load_content_graph()
    active = gen_spec._active_domain()
    domain_name = active.name if active is not None else ""
    outputs = {
        "REQUIREMENTS.md": gen_spec.build_requirements(g),
        "TENSIONS.md": gen_spec.build_tensions(g),
        "OPEN.md": gen_spec.build_open(g),
        "UNENFORCED.md": gen_spec.build_unenforced(g),
        "GLOSSARY.md": gen_spec.build_glossary(g),
        "HISTORY.md": gen_spec.build_history(g),
        "DECISIONS.md": gen_spec.build_decisions(g),
        "CONSTITUTION.md": gen_spec.build_constitution(g),
        "ENTITIES.md": gen_spec.build_entities_md(g, domain_name),
        "atoms/operator.md": gen_spec.build_methodology_atoms_operator(g),
        "atoms/substrate.md": gen_spec.build_methodology_atoms_substrate(g),
        "atoms/discipline.md": gen_spec.build_methodology_atoms_discipline(g),
        "atoms/check.md": gen_spec.build_methodology_atoms_check(g),
    }
    for name, text in outputs.items():
        p = tmp_path / name
        p.parent.mkdir(parents=True, exist_ok=True)
        gen_spec._write(p, text)
        raw = p.read_bytes()
        assert b"\r\n" not in raw, f"{name}: CRLF found — must be LF"
        assert raw.decode("utf-8").startswith(gen_spec.BANNER), (
            f"{name}: missing AUTOGENERATED banner"
        )


