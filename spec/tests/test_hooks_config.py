def _tool_name(command: str):
    """Extract spec/tools/<name>.py from a hook command."""
    import re
    m = re.search(r"tools[/\\]([A-Za-z0-9_]+)\.py", command)
    return m.group(1) if m else None


def test_no_duplicate_hooks_across_committed_and_local():
    """No tool should run twice on the same event (UX#2 regression guard)."""
    duplicates = []
    for path_label, path in [("committed", _SETTINGS_COMMITTED), ("local", _SETTINGS_LOCAL)]:
        pass  # just loading below

    committed = json.loads(_SETTINGS_COMMITTED.read_text(encoding="utf-8")) if _SETTINGS_COMMITTED.exists() else {}
    local = json.loads(_SETTINGS_LOCAL.read_text(encoding="utf-8")) if _SETTINGS_LOCAL.exists() else {}

    for event in set(list(committed.get("hooks", {}).keys()) + list(local.get("hooks", {}).keys())):
        committed_tools = set()
        for g in committed.get("hooks", {}).get(event, []):
            for h in g.get("hooks", []):
                t = _tool_name(h.get("command", ""))
                if t:
                    committed_tools.add(t)
        for g in local.get("hooks", {}).get(event, []):
            for h in g.get("hooks", []):
                t = _tool_name(h.get("command", ""))
                if t and t in committed_tools:
                    duplicates.append(f"{event}: {t}")

    assert not duplicates, f"Hooks duplicated across committed+local (will run twice): {duplicates}"


def test_emit_cipher_returns_valid_json():
    result = subprocess.run(
        [sys.executable, str(_EMIT_CIPHER)],
        capture_output=True,
        text=True,
        timeout=30,
    )
    assert result.returncode == 0, f"emit_cipher.py failed: {result.stderr}"
    data = json.loads(result.stdout.strip())
    assert data["hookSpecificOutput"]["hookEventName"] == "UserPromptSubmit"
    assert "additionalContext" in data["hookSpecificOutput"]
