"""Tests for .claude/ hooks configuration (committed + local merged view).

Claude Code MERGES .claude/settings.json (committed sensorium, generated by
setup_hooks.py) and .claude/settings.local.json (personal, git-ignored).  The
tests below verify the MERGED view: every required hook must be present in at
least one of the two files, and no hook should run twice (no duplicates across
files for the same event+tool).
"""

import json
import subprocess
import sys
from pathlib import Path

_CLAUDE_DIR = Path(__file__).resolve().parents[2] / ".claude"
_SETTINGS_COMMITTED = _CLAUDE_DIR / "settings.json"
_SETTINGS_LOCAL = _CLAUDE_DIR / "settings.local.json"
_EMIT_CIPHER = Path(__file__).resolve().parents[1] / "tools" / "emit_cipher.py"


def _load_settings() -> dict:
    """Return the merged hooks view (committed + local), same as Claude Code."""
    merged: dict = {}
    for path in (_SETTINGS_COMMITTED, _SETTINGS_LOCAL):
        if not path.exists():
            continue
        data = json.loads(path.read_text(encoding="utf-8"))
        for event, groups in data.get("hooks", {}).items():
            merged.setdefault(event, []).extend(groups)
    return {"hooks": merged}


def test_committed_settings_json_exists_and_valid():
    assert _SETTINGS_COMMITTED.exists(), f"Missing: {_SETTINGS_COMMITTED}"
    data = json.loads(_SETTINGS_COMMITTED.read_text(encoding="utf-8"))
    assert isinstance(data, dict)
    assert "hooks" in data


def test_session_start_hook_runs_gen_spec():
    data = _load_settings()
    hooks = data["hooks"].get("SessionStart", [])
    commands = [
        h["command"]
        for entry in hooks
        for h in entry.get("hooks", [])
        if h.get("type") == "command"
    ]
    assert any("gen_spec.py" in cmd for cmd in commands), (
        f"No gen_spec.py in SessionStart commands: {commands}"
    )


